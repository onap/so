package org.onap.so.apihandlerinfra;

import lombok.RequiredArgsConstructor;
import org.onap.so.db.request.beans.InfraActiveRequests;
import org.onap.so.db.request.data.repository.InfraActiveRequestsRepository;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Component;
import java.util.List;

@Component
@RequiredArgsConstructor
public class InfraActiveRequestsService {
    private static final Logger logger = LoggerFactory.getLogger(InfraActiveRequestsService.class);

    @Autowired
    private final InfraActiveRequestsRepository infraActiveRequestsRepository;

    protected List<InfraActiveRequests> getListOfRequestsInProgress() {
        logger.info("InfraActiveRequestsService is returning list of in progress request:");
        return infraActiveRequestsRepository.getListOfRequestsInProgress();
    }
}
