package org.onap.so.apihandlerinfra;

import org.apache.hc.client5.http.impl.classic.CloseableHttpClient;
import org.apache.hc.client5.http.impl.classic.HttpClients;
import org.apache.hc.client5.http.impl.io.PoolingHttpClientConnectionManagerBuilder;
import org.springframework.beans.factory.SmartInitializingSingleton;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.context.annotation.Profile;
import org.springframework.http.client.BufferingClientHttpRequestFactory;
import org.springframework.http.client.HttpComponentsClientHttpRequestFactory;
import org.springframework.web.client.RestTemplate;

@Configuration
@Profile("test")
public class TestRestTemplateConfiguration {

    @Bean
    public SmartInitializingSingleton disableRestTemplateConnectionReuse(RestTemplate restTemplate) {
        return () -> {
            CloseableHttpClient httpClient = HttpClients.custom().setConnectionManager(
                    PoolingHttpClientConnectionManagerBuilder.create().setMaxConnPerRoute(1).setMaxConnTotal(1).build())
                    .build();
            HttpComponentsClientHttpRequestFactory requestFactory =
                    new HttpComponentsClientHttpRequestFactory(httpClient);
            restTemplate.setRequestFactory(new BufferingClientHttpRequestFactory(requestFactory));
        };
    }
}
